# Step 1: Assign specific values to the variables
number1 = 10
number2 = 5

# Step 2: Perform the arithmetic operations
add = number1 + number2
sub = number1 - number2
mul = number1 * number2

# Step 3: Print the results in a human-readable format
print("Addition of", number1, "and", number2, "is", add)
print("Subtraction of", number1, "and", number2, "is", sub)
print("Multiplication of", number1, "and", number2, "is", mul)
